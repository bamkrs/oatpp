v_io_size Huffman::decode(oatpp::String& to, Payload::const_iterator src, Payload::const_iterator max) {
  static const auto initial = Huffman::DecodeTableEntry(false, true, 0, 0);
  const auto start = src;
  auto t = &initial;
  v_uint8 c;
  v_io_size count = 0;

  to = oatpp::String((unsigned long)(max-src)*2); // worst case

  /* We use the decoding algorithm described in
     http://graphics.ics.uci.edu/pub/Prefix.pdf */
  for (; src != max;) {
    c = *src++;
    t = &Huffman::decodeTable[t->id][c >> 4];
    if (t->yield) {
      to->at(count) = t->symbol;
      ++count;
    }

    t = &Huffman::decodeTable[t->id][c & 0xf];
    if (t->yield) {
      to->at(count) = t->symbol;
      ++count;
    }
  }

  if (!(t->accepted)) {
    return -1;
  }
  to->resize(count);
  return (v_io_size)(src-start);
}

v_io_size Huffman::decode(p_uint8 to, v_io_size len, Payload::const_iterator src, Payload::const_iterator max) {
  static const auto initial = Huffman::DecodeTableEntry(false, true, 0, 0);
  const auto start = src;
  auto t = &initial;
  v_uint8 c;

  /* We use the decoding algorithm described in
     http://graphics.ics.uci.edu/pub/Prefix.pdf */
  for (; src != max && len > 1;) {
    c = *src++;
    t = &Huffman::decodeTable[t->id][c >> 4];
    if (t->yield) {
      *to++ = t->symbol;
      --len;
    }

    t = &Huffman::decodeTable[t->id][c & 0xf];
    if (t->yield) {
      *to++ = t->symbol;
      --len;
    }
  }

  if (!(t->accepted)) {
    return -1;
  }

  return (v_io_size)(start-src);
}

v_io_size Huffman::decode(oatpp::String &to,
                          v_io_size stringSize,
                          const std::shared_ptr<data::stream::BufferedInputStream> &stream) {
  static const auto initial = Huffman::DecodeTableEntry(false, true, 0, 0);
  v_io_size count = 0;
  auto t = &initial;
  to = oatpp::String(stringSize * 2); // worst case
  std::vector<v_uint8> octets(stringSize);
  stream->readExactSizeDataSimple(octets.data(), stringSize);

  /* We use the decoding algorithm described in
     http://graphics.ics.uci.edu/pub/Prefix.pdf */
  for (auto it = octets.begin(); it != octets.end(); ++it) {
    v_uint8 c = *it;
    t = &Huffman::decodeTable[t->id][c >> 4];
    if (t->yield) {
      to->at(count++) = t->symbol;
    }

    t = &Huffman::decodeTable[t->id][c & 0xf];
    if (t->yield) {
      to->at(count++) = t->symbol;
    }
  }

  if (!(t->accepted)) {
    return -1;
  }
  to->resize(count);
  return stringSize;
}

v_io_size Huffman::encode(Payload &to, const String &src) {
  return encode(to, (p_uint8)src->data(), src->length());
}

v_io_size Huffman::encode(Payload &to, p_uint8 src, v_io_size nstr) {
    const Symbol *sym;
    p_uint8 ptr = src;
    const v_uint8 *end = src + nstr;
    v_uint64 code = 0;
    v_uint32 nbits = 0;
    v_io_size actual = 0;

    for (; ptr != end;) {
      sym = &symbolTable[*ptr++];
      code |= (uint64_t)sym->code << (32 - nbits);
      nbits += sym->bits;
      if (nbits < 32) {
        continue;
      }

      v_uint32 net = htonl((uint32_t)(code >> 32));
      auto xptr = (p_uint8)&net;
      to.insert(to.end(), xptr, xptr + 4);
      code <<= 32;
      nbits -= 32;
      actual += 4;

      for (; nbits >= 8;) {
        to.emplace_back((v_uint8)(code >> 56));
        code <<= 8;
        nbits -= 8;
        ++actual;
      }
    }

    for (; nbits >= 8;) {
      to.emplace_back((v_uint8)(code >> 56));
      code <<= 8;
      nbits -= 8;
      ++actual;
    }

    if (nbits) {
      to.emplace_back((uint8_t)((uint8_t)(code >> 56) | ((1 << (8 - nbits)) - 1)));
      ++actual;
    }

    return actual;
}

v_io_size Huffman::calculateSize(const p_uint8 src, v_io_size len) {
  size_t i;
  size_t nbits = 0;
  for (i = 0; i < len; ++i) {
    nbits += symbolTable[src[i]].bits;
  }
  return (nbits + 7) / 8;
}

